    private void loadPeliculas() {
        ArrayList<Film> films = cine.getFilms(); // Use direct Cine instance
        DefaultListModel<String> model = new DefaultListModel<>();
        
        if (films != null && !films.isEmpty()) {
            for (Film film : films) {
                model.addElement(film.getNombre() + " - " + film.getDuracion() + " min");
            }
        } else {
            model.addElement("No hay pel√≠culas disponibles");
        }
        
        jListVerPeliculas.setModel(model);
    }

